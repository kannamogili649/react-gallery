class Gallery extends Component {
  state = {activeTabId: imagesList[0].id}

  getfilteredTab = () => {
    const {activeTabId} = this.state
    const filteredTab = imagesList.filter(
      eachItem => eachItem.id === activeTabId,
    )
    return filteredTab
  }

  updateActiveTab = id => this.setState({activeTabId: id})

  render() {
    const {activeTabId} = this.state
    const filteredTab = this.getfilteredTab()

    return (
      <div className="container">
        <div className="content-container">
          <div className="img-container">
            {filteredTab.map(eachItem => (
              <img
                src={eachItem.imageUrl}
                alt={eachItem.imageAltText}
                className="image"
              />
            ))}
          </div>
          <h1>Nature Photography</h1>
          <p>Nature Photography by Rahul</p>
          <ul className="thumbnail-container-list">
            {imagesList.map(eachThumbnail => (
              <ThumbnailItem
                updateActiveTab={this.updateActiveTab}
                thumbnail={eachThumbnail}
                key={eachThumbnail.id}
                isActive={activeTabId === eachThumbnail.id}
              />
            ))}
          </ul>
        </div>
      </div>
    )
  }
}
export default Gallery
